-module(nostr_mnesia).
-export([start/0]).

%%--------------------------------------------------------------------
%% @doc
%% @end
%%--------------------------------------------------------------------
-spec start() -> any().

start() ->
    application:ensure_all_started(mnesia),
    [ Table:create_table() || Table <- tables() ].

tables() ->
    ['nostr@clients'
    ].
